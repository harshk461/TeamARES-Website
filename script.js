function mySubmit() {
  var form = document.getElementById('contact-form');
  var inputs = form.getElementsByTagName('input');
  var textArea = document.getElementById('contact-message');
  var email = inputs.email.value;
  var subject = inputs.subject.value;
  var name = inputs.name.value;
  var text = textArea.value;
  var data = {
    "access_token": "w5llah6rbha5y34tgjc3c30x"
  };
  data['subject'] = subject;
  data['text'] = "From: " + name + " Email: " + email + " " + text;
  $.post('https://postmail.invotes.com/send',
    data,
    onSuccess
  ).fail(onError);

  return false;
}



// For Team Filter
function f(color) {
  abc = document.getElementsByClassName('outdiv');
  if (color == 'all') {
    abc.forEach(element => {
      element.style.display = 'inline-block';
    });
  }
  else {
    abc.forEach(element => {
      element.style.display = 'none'
      var c = element.getAttribute('data-color');
      if (c == color) {
        element.style.display = 'inline-block';
      }

    });
  }
};